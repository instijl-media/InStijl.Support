import fs from 'node:fs';
import path from 'node:path';
import type {SidebarsConfig} from '@docusaurus/plugin-content-docs';

/**
 * One sidebar per app folder, so a doc page only shows pages within that app
 * (not all the other apps). Each sidebar id matches the app slug.
 */
const root = path.resolve(__dirname, 'content/apps');

const slugs = fs.existsSync(root)
  ? fs
      .readdirSync(root, {withFileTypes: true})
      .filter(
        (e) =>
          e.isDirectory() && fs.existsSync(path.join(root, e.name, 'index.mdx')),
      )
      .map((e) => e.name)
  : [];

const sidebars: SidebarsConfig = Object.fromEntries(
  slugs.map((slug) => [slug, [{type: 'autogenerated', dirName: slug}]]),
);

export default sidebars;
